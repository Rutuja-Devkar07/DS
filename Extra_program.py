# 1. Comparing Distributions:
# Histograms: From the histograms, you can observe how each feature is distributed. Typically:
# Normal Distribution: A bell-shaped curve (e.g., petal length) would suggest a normal distribution.
# Skewed Distribution: If one tail is longer, the feature is skewed. For example, if petal width shows
# a longer right tail, it might be positively skewed.
# Multi-modal Distribution: Multiple peaks can suggest the presence of different groups within the data
# (such as species), which might indicate a different distribution for each group.
# Distribution Characteristics: Histograms will give you a rough idea of the spread, skewness, and
# central tendency (mean/median) of each feature.

# 2. Identifying Outliers:
# Boxplots: Outliers can be identified in boxplots as points that fall outside the "whiskers"
# (i.e., 1.5 times the interquartile range above the upper quartile or below the lower quartile).
# Points outside this range are considered outliers.
# Outliers in Features: If you notice any data points that fall significantly above or below
# the boxplot's whiskers, these points are potential outliers. For example:
# Sepal Length: Might have outliers if there are a few extremely long or short sepals.
# Petal Width: If you see a single point far from the rest of the data, it could be an outlier
# (for instance, a very large petal width).
# By examining both histograms and boxplots together, you can:

# Histograms show the overall distribution, helping you see whether the feature is normally distributed,
# skewed, or multimodal.
# Boxplots help you identify outliers by highlighting points beyond the whiskers. These outliers
# # might need further investigation, as they can either represent anomalies or significant data points worth studying.

