import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# # 2. Load the Iris dataset
# from sklearn.datasets import load_iris

# Load the Iris dataset from a URL
url = "https://archive.ics.uci.edu/ml/machine-learning-databases/iris/iris.data"
column_names = ['sepal_length', 'sepal_width', 'petal_length', 'petal_width', 'species']
iris = pd.read_csv(url, header=None, names=column_names)

# 1. List down the features and their types (e.g., numeric, nominal) available in the dataset.
print("Features and their types:")
print(iris.dtypes)

# 2. Create histograms for each feature
iris_features = ['sepal_length', 'sepal_width', 'petal_length', 'petal_width']
iris[iris_features].hist(bins=20, figsize=(12, 10))
plt.suptitle('Histograms of Iris Features')
plt.show()

# 3. Create boxplots for each feature
plt.figure(figsize=(12, 8))
for i, feature in enumerate(iris_features, 1):
    plt.subplot(2, 2, i)
    sns.boxplot(data=iris, x='species', y=feature)
    plt.title(f'Boxplot for {feature}')
plt.tight_layout()
plt.show()

